#include <iostream>
using namespace std;

int main()
{
	int a,b,c,d;
	a=10, b=10, c=10, d=10;
	
	
// increment
	// postincrement
	cout << a << endl;
	cout << a++ << endl;
	cout << a << endl<<endl;
	
	// preincrement
	cout << b << endl;
	cout << ++b << endl;
	cout << b << endl<<endl;
	
// decrement
	// postincrement
	cout << c << endl;
	cout << c-- << endl;
	cout << c << endl<<endl;
	
	// preincrement
	cout << d << endl;
	cout << --d << endl;
	cout << d << endl<<endl;
	
}
